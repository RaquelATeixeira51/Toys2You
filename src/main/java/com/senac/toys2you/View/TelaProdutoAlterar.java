    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(txtPK_ID.getText().isBlank()){
            return;
        }
        if(txtDS_NOMEPRODUTO.getText().isBlank()){
            return;
        }
        if(txtFK_TIPO.getText().isBlank()){
            return;
        }
        if(txtVL_VALOR.getText().isBlank()){
            return;
        }
        tipo.setId(Integer.valueOf(txtFK_TIPO.getText()));
                
        t.setNome(txtDS_NOMEPRODUTO.getText());
        t.setTipo(tipo);
        t.setValor(Double.valueOf(txtVL_VALOR.getText()));
        t.setDescricao(txtDS_DESCRICAO.getText());
        
        if(!t.getId().isBlank()){
            toy.alteraProduto(txtPK_ID.getText(), t.getNome(), t.getDescricao(), t.getValor(), tipo);
        }else
            t.setId(txtPK_ID.getText());
            toy.adicionaProduto(t,tipo.getId());
       
        new TelaProdutoAlterar().dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

